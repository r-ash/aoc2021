test_that("string diff util works", {
  expect_equal(str_diff("abc", "a"), "bc")
  expect_equal(str_diff("abc", "ad"), "bc")
  expect_equal(str_diff("abc", "abc"), "")
})

test_that("string in util works", {
  expect_true(str_in("abc", "a"))
  expect_true(str_in("abc", "abc"))
  expect_true(str_in("abc", "bca"))
  expect_false(str_in("abc", "d"))
  expect_false(str_in("abc", "abd"))
})

